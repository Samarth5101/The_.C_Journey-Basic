//+ - /(see dividig operation) * %(modular division operator - shows remainder) calcultor of two specified numbers
#include <stdio.h>

int main()
{
    int a = 2;
    int b = 5;
    printf("The value of a + b is %d\n", a + b);
    printf("The value of a - b is %d\n", a - b);
    printf("The value of a * b is %d\n", a * b);
    printf("The value of a / b is %d\n", a / b);
    printf("a when divided by b  leaves remainder as %d\n", a % b);
    return 0;
}