/*dividing of two real no. but to show decimal value 
we use real numer as decimal value(for numerator) and float for accurate result otherwise
with int it will give GIF value. */

#include <stdio.h>

int main()
{
    printf("the value of 51/2 is %f\n", 51.0 / 2);
    printf("the value of 5/2 is %f\n", 5.0 / 2);
    return 0;
}